def _run_automatic_movie(scraper: Scraper, movie_dir: Path) -> Any:
    """Drive the automatic ``scrape_movie`` path with fake providers + stubbed I/O."""
    with (
        patch("personalscraper.scraper.confidence.match_movie_detailed", return_value=(_movie_match(), [])),
        patch.object(scraper._registry.get("tmdb"), "get_movie", return_value=_MOVIE_DATA),
        patch("personalscraper.scraper.scraper.extract_stream_info", return_value=None),
        patch.object(scraper._artwork, "download_image", side_effect=_fake_download_image),
    ):
        return scraper.scrape_movie(movie_dir)


def test_movie_writeback_pins(scraper: Scraper, tmp_path: Path) -> None:
    """A raw movie folder scrapes to canonical folder + video + NFO ids + artwork.

    Pins (against ``golden/movie_writeback.json``): the final ``Title (Year)``
    folder name, the renamed ``Title.ext`` video, the movie NFO ``<title>`` /
    ``<year>`` / ``<uniqueid>`` (imdb ``default="true"`` + tmdb), and the poster
    + landscape artwork files that land next to the media.
    """
    movie_dir = tmp_path / "the.matrix.1999.1080p.BluRay.x264-GROUP"
    movie_dir.mkdir()
    (movie_dir / "the.matrix.1999.1080p.BluRay.x264-GROUP.mkv").write_bytes(b"\x00")

    result = _run_automatic_movie(scraper, movie_dir)

    assert result.action == "scraped", result.error
    snapshot = _movie_snapshot(result.media_path)
    _check_golden("movie_writeback", snapshot)


def test_missing_golden_fails_not_skips(tmp_path: Path) -> None:
    """A missing golden must FAIL (AssertionError), never silently pass/skip."""
    if _CAPTURE:
        pytest.skip("capture mode writes goldens instead of asserting")
    with pytest.raises(AssertionError, match="MISSING"):
        _check_golden("does_not_exist", {"any": "value"}, golden_dir=tmp_path / "empty")
