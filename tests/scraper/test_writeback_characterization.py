"""Characterization pins for the scraper post-match write-back (P0.5).

Locks the CURRENT behaviour of the canonical write path shared by the
automatic scrape and the operator-forced resolve — folder rename, video /
episode rename, ``<title>`` / ``<year>`` / ``<uniqueid>`` NFO fields, and the
set of artwork files that land next to the media — so any later thin refactor
of ``_write_confirmed_movie`` / ``_write_confirmed_show`` that changes an
observable output trips a golden mismatch instead of shipping silently. This
file changes ZERO production code; it only observes.

Golden mechanics mirror :mod:`tests.verify.test_characterization_golden`:

* Run-mode (default): load each golden JSON and assert an exact match. A
  missing golden **fails** the test (never skips) — a vanished baseline must
  not mask a regression.
* Capture-mode (``CAPTURE_GOLDEN=1``): (re)write the golden files from the
  CURRENT code. The committed goldens are never auto-regenerated by a normal
  run.

The write path reaches the network only through the provider clients and the
artwork downloader; both are stubbed here (fake providers via ``mock_registry``,
``download_image`` replaced by a local stub that writes deterministic bytes), so
the suite is fully hermetic — no TMDB/TVDB, no ffprobe, no real staging.
"""

