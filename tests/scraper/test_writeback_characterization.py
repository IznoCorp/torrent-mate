def _fake_download_image(url: str, dest: Path) -> bool:
    """Stand in for ``ArtworkDownloader.download_image`` without any network.

    Writes deterministic placeholder bytes to *dest* (creating parents) so the
    real ``download_movie_artwork`` / ``download_tvshow_artwork`` naming logic
    runs end-to-end and the resulting artwork files actually land next to the
    media — exactly what the snapshot pins — while staying fully offline.

    Args:
        url: Source URL (ignored; kept for signature parity).
        dest: Destination path the downloader computed via ``NamingPatterns``.

    Returns:
        Always ``True`` (a successful "download"), matching the contract the
        downloader relies on to append *dest* to its returned list.
    """
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_bytes(b"stub-image")
    return True


def _tree(media_path: Path) -> list[str]:
    """Return the sorted relative paths of every file under *media_path*.

    Pins the write-back's filesystem shape (renamed video/episodes, NFO
    presence, the artwork set) without capturing volatile file *contents* —
    only the deterministic names and layout.
    """
    return sorted(str(p.relative_to(media_path)) for p in media_path.rglob("*") if p.is_file())


def _nfo_pins(nfo_path: Path) -> dict[str, Any]:
    """Extract the stable identity pins from an NFO file.

    Captures ``<title>``, ``<year>`` and each ``<uniqueid>`` (id text + type +
    ``default`` flag). These are the fields dispatch/verify key on; volatile
    fields (dates already deterministic in the fixtures, generator block) are
    intentionally not pinned here.

    Args:
        nfo_path: Path to the NFO file to parse.

    Returns:
        A JSON-able dict with ``title``, ``year`` and a ``uniqueids`` map
        keyed by ``type``.
    """
    root = ET.parse(nfo_path).getroot()
    uniqueids = {u.get("type"): {"id": u.text, "default": u.get("default")} for u in root.findall("uniqueid")}
    return {
        "title": root.findtext("title"),
        "year": root.findtext("year"),
        "uniqueids": uniqueids,
    }


def _movie_snapshot(media_path: Path) -> dict[str, Any]:
    """Normalized write-back snapshot for a movie directory."""
    nfo = next(media_path.glob("*.nfo"))
    return {
        "folder": media_path.name,
        "tree": _tree(media_path),
        "nfo": _nfo_pins(nfo),
    }


def _check_golden(name: str, actual: dict[str, Any], *, golden_dir: Path = GOLDEN_DIR) -> None:
    """Capture or assert one normalized snapshot against its golden JSON.

    Mirrors :func:`tests.verify.test_characterization_golden._check`: in
    capture mode writes the golden; otherwise asserts the golden EXISTS
    (fail-on-missing, never skip) then asserts an exact match.

    Args:
        name: Golden basename (``<name>.json``).
        actual: The JSON-able snapshot produced by the current code.
        golden_dir: Directory holding the golden files (overridable so the
            fail-on-missing behaviour is itself testable against a tmp dir).
    """
    path = golden_dir / f"{name}.json"
    if _CAPTURE:
        golden_dir.mkdir(parents=True, exist_ok=True)
        path.write_text(json.dumps(actual, indent=2, sort_keys=True))
        return
    assert path.exists(), f"Golden {name}.json MISSING — capture it (CAPTURE_GOLDEN=1) before asserting"
    assert actual == json.loads(path.read_text()), f"Golden mismatch for {name}"


